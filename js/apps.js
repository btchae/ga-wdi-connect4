var gameBoard = new Array();
var makeBoard =function(){
  gameBoard = new Array();
  for(var i=0; i<6; i++){
    gameBoard[i] = new Array();
    for(var j=0; j<7; j++){
      gameBoard[i].push(0);
    }
  }
}
makeBoard();
